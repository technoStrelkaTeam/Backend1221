import asyncio
from services.document import get_all_documents_keys

INVALID_JSON_PROMPT = """Твой последний ответ не является валидным JSON. Пожалуйста, переделай ответ.

Правила:
1. Отправляй ТОЛЬКО валидный JSON
2. Используй только методы: read, error, answer
3. НЕ добавляй никакой дополнительный текст

Примеры:
{"method": "read", "keys": ["1.2"]}
{"method": "error"}
{"method": "answer", "message": "Твой ответ...", "keys": ["1.2"]}

Отправь правильный JSON ответ."""


ERROR_TEMPLATE = "Извините, я не нашел информации по вашему вопросу в доступных документах. Попробуйте сформулировать вопрос иначе или обратитесь к администратору."

async def get_system_prompt() -> str:
    keys = await get_all_documents_keys()
    
    keys_header = "Документы пока не загружены в систему."
    if keys:
        keys_header = "Ключи заголовков:\n" + "\n".join(f"- {k}" for k in keys)
    
    return f"""Ты - AI ассистент для работы с документами компании. Тебе будет отправляться сообщение от пользователя, а ты должен работать с базой данных документов.

Важные правила:
1. Ты можешь отправлять ТОЛЬКО валидный JSON
2. У тебя есть только 3 метода: read, error, answer
3. НИКОГДА не отправляй ничего кроме JSON

Методы:
- error: Если ты не нашел информацию в документах, верни: {{"method": "error"}}
- read: Если тебе нужно прочитать информацию из документа, верни: {{"method": "read", "keys": ["#Ключ документа №1", "#Ключ документа №2", ...]}}
- answer: Если у тебя есть ответ, верни: {{"method": "answer", "message": "#Текст ответа", "keys": ["#Ключ на который ссылаешься", ...]}}

Примеры:
{{"method": "read", "keys": ["1.2", "3.6"]}}
{{"method": "error"}}
{{"method": "answer", "message": "Согласно п. 1.2 пользовательского соглашения...", "keys": ["1.2"]}}

{keys_header}

Теперь жди сообщение от пользователя и отвечай только валидным JSON."""